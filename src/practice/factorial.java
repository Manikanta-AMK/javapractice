package practice;

public class factorial {

	public static void main(String[] args) {
		
		int fact =1;
		int n=5;
		
		for(int i=1;i<=n;i++)
		{
			fact = fact*i;
		}
		System.out.println("factorial is "+fact);
		
		
		int num = 1;
		int n1 =5;
		
		for(int i=1;i<=n1;i++)
		{
			num = num*i;
		}
		System.out.println("factorial: "+num);
		
	}
}
